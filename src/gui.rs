//! Simple framebuffer GUI. TS RULE: GUI ops prioritized by node weight — kernel supremacy.

use crate::ts;

/// Min weight for any draw op (clear, rect, text).
const MIN_WEIGHT_GUI_DRAW: f32 = 0.55;

/// 256-color VGA index (mode 13h).
#[derive(Clone, Copy)]
pub struct Color(pub u8);

pub const COLOR_BLACK: Color = Color(0);
pub const COLOR_WHITE: Color = Color(15);
pub const COLOR_BLUE: Color = Color(1);
pub const COLOR_GREEN: Color = Color(2);
pub const COLOR_RED: Color = Color(4);
pub const COLOR_YELLOW: Color = Color(14);
pub const COLOR_CYAN: Color = Color(3);

/// Framebuffer info: linear buffer at base, width/height, bytes per pixel, stride.
pub struct FramebufferInfo {
    pub base: *mut u8,
    pub width: u32,
    pub height: u32,
    pub bytes_per_pixel: u32,
    pub stride: u32,
}

unsafe impl Send for FramebufferInfo {}

/// TS RULE: GUI ops prioritized by node weight — kernel supremacy. All draw methods check min weight 0.55.
pub struct FrameBufferWriter {
    info: FramebufferInfo,
}

impl FrameBufferWriter {
    pub fn new(info: FramebufferInfo) -> Self {
        FrameBufferWriter { info }
    }

    fn check_weight(&self) -> Result<(), ()> {
        ts::enforce_min_weight("gui draw", MIN_WEIGHT_GUI_DRAW)
    }

    fn set_pixel_unsafe(&mut self, x: u32, y: u32, color: Color) {
        if x >= self.info.width || y >= self.info.height {
            return;
        }
        let offset = (y as u64 * self.info.stride as u64 + x as u64 * self.info.bytes_per_pixel as u64) as usize;
        unsafe {
            *self.info.base.add(offset) = color.0;
        }
    }

    /// Clear entire screen to color.
    pub fn clear_screen(&mut self, color: Color) -> Result<(), ()> {
        self.check_weight()?;
        for y in 0..self.info.height {
            for x in 0..self.info.width {
                self.set_pixel_unsafe(x, y, color);
            }
        }
        Ok(())
    }

    /// Draw a filled rectangle.
    pub fn draw_rect(&mut self, x: u32, y: u32, w: u32, h: u32, color: Color) -> Result<(), ()> {
        self.check_weight()?;
        for dy in 0..h {
            for dx in 0..w {
                self.set_pixel_unsafe(x + dx, y + dy, color);
            }
        }
        Ok(())
    }

    /// Draw text at (x,y) using minimal 8x8 font. Only printable ASCII.
    pub fn draw_text(&mut self, x: u32, y: u32, s: &str, color: Color) -> Result<(), ()> {
        self.check_weight()?;
        let mut px = x;
        for c in s.bytes() {
            if c >= 32 && c < 128 {
                let idx = ((c - 32) as usize).min(MINIMAL_FONT.len().wrapping_sub(1));
                let rows = &MINIMAL_FONT[idx];
                for (row, &bits) in rows.iter().enumerate() {
                    let yy = y + row as u32;
                    for col in 0..8u8 {
                        if (bits >> (7 - col)) & 1 != 0 {
                            self.set_pixel_unsafe(px + col as u32, yy, color);
                        }
                    }
                }
            }
            px += 8;
        }
        Ok(())
    }
}

